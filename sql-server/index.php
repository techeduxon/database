			    <h6 class="mb-10">What are the latest/key features in SQL Server 2019 ?</h6>
<p>Like all the previous versions, the Microsoft SQL Server 2019 latest version is also jam-packed with new features and capabilities.
</p>							

							<ul class="check-list">
                                    <li class="list-item">Accelerated Database Recovery (ADR) - the SQL team has redeveloped how recovery works and has dramatically decreased how long this process takes.</li>
                                    <li class="list-item">AlwaysEncrypted - SQL Server can now securely encrypt a portion of memory to perform computations on these encrypted columns without ever exposing the unencrypted values to the rest of the processes (or administrators).</li>
                                    <li class="list-item">Performance improvement changes - There’s a new index optimization for indexes that have a sequential key, temp table recompilation improvements, improved indirect checkpoint scalability and more.
</li>
                                    <li class="list-item">Support for replication - SQL Server on Linux supports replication from 2019 version and this is a major addition!
</li>
<li class="list-item"> Optimization in tempDB - the tempdb’s system tables can be moved into memory-optimized tables taking the pressure off the disks. This helps handle heavy workload with ease.</li>
                                
